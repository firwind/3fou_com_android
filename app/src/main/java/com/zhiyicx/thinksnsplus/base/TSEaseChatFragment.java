    /**
     * 本地加载更多
     */
    private void loadMoreLocalMessage() {
        if (listView.getFirstVisiblePosition() == 0 && !isloading && haveMoreData) {
            List<EMMessage> messages;
            try {
                messages = conversation.loadMoreMsgFromDB(conversation.getAllMessages().size() == 0 ? "" : conversation.getAllMessages().get(0)
                                .getMsgId(),
                        pagesize);
            } catch (Exception e1) {
                swipeRefreshLayout.setRefreshing(false);
                return;
            }
            if (messages.size() > 0) {
                messageList.refreshSeekTo(messages.size() - 1);
                if (messages.size() != pagesize) {
                    haveMoreData = false;
                }
            } else {
                haveMoreData = false;
            }

            isloading = false;
        } else {
            ToastUtils.showToast(getString(R.string.no_more_messages));
        }
        swipeRefreshLayout.setRefreshing(false);
    }

    private void loadMoreRoamingMessages() {
        if (!haveMoreData) {
            ToastUtils.showToast(getString(R.string.no_more_messages));
            swipeRefreshLayout.setRefreshing(false);
            return;
        }
        if (fetchQueue != null) {
            fetchQueue.execute(() -> {
                try {
                    List<EMMessage> messages = conversation.getAllMessages();
                    EMClient.getInstance().chatManager().fetchHistoryMessages(
                            toChatUsername, EaseCommonUtils.getConversationType(chatType), pagesize,
                            (messages != null && messages.size() > 0) ? messages.get(0).getMsgId() : "");
                } catch (HyphenateException e) {
                    e.printStackTrace();
                } finally {
                    Activity activity = getActivity();
                    if (activity != null) {
                        activity.runOnUiThread(this::loadMoreLocalMessage);
                    }
                }
            });
        }
    }

    @Override
    public void onActivityResult(int requestCode, int resultCode, Intent data) {
        super.onActivityResult(requestCode, resultCode, data);

        if(null != mPhotoSelector)
            mPhotoSelector.onActivityResult(requestCode,resultCode,data);

        if (resultCode == Activity.RESULT_OK) {
            if (requestCode == REQUEST_CODE_CAMERA) { // capture new image
                if (cameraFile != null && cameraFile.exists()) {
                    sendImageMessage(cameraFile.getAbsolutePath());
                }
            } /*else if (requestCode == REQUEST_CODE_LOCAL) { // send local image---这里改用PhotoSelector
                if (data != null) {
                    Uri selectedImage = data.getData();
                    if (selectedImage != null) {
                        sendPicByUri(selectedImage);
                    }
                }
            }*/ else if (requestCode == REQUEST_CODE_MAP) { // location
                if (data == null) {
                    return;
                }
                if (!data.hasExtra("latitude")) {
                    ToastUtils.showToast(R.string.unable_to_get_loaction);
                    return;
                }
                double latitude = data.getDoubleExtra("latitude", 0);
                double longitude = data.getDoubleExtra("longitude", 0);
                String locationAddress = data.getStringExtra("address");
                if (locationAddress != null && !locationAddress.equals("")) {
                    sendLocationMessage(latitude, longitude, locationAddress);
                } else {
                    ToastUtils.showToast(R.string.unable_to_get_loaction);
                }

            }
        }
    }


    @Override
    public void onResume() {
        super.onResume();
        TSEMHyphenate.getInstance().setToChatUsername(toChatUsername);
        // 第一次不调用这个
        if (mIsNeedRefreshToLast) {
            resumeRefreshMessageList();
        }
        mIsNeedRefreshToLast = true;
        EaseUI.getInstance().pushActivity(getActivity());
        // register the event listener when enter the foreground
        EMClient.getInstance().chatManager().removeMessageListener(this);
        EMClient.getInstance().chatManager().addMessageListener(this);

        if (chatType == EaseConstant.CHATTYPE_GROUP) {
            EaseAtMessageHelper.get().removeAtMeGroup(toChatUsername);
        }
    }

    protected void resumeRefreshMessageList() {
        if (isMessageListInited) {
            messageList.refreshSelectLast();
        }
    }

